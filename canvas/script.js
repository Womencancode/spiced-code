////////////////////////////////////////////////////////////
///////     STEP 1 - LINK UP JS TO HTML      ///////////////
////////////////////////////////////////////////////////////
// console.log("sanity check");
////////////////////////////////////////////////////////////
///////     STEP 2 - RENDER THE CONTEXT      ///////////////
////////////////////////////////////////////////////////////
var ctx = document.getElementById("canvas").getContext("2d");
// console.log(ctx);
////////////////////////////////////////////////////////////
///////     STEP 3 - BEGIN THE PATH          ///////////////
////////////////////////////////////////////////////////////
ctx.beginPath();
////////////////////////////////////////////////////////////
//// STEP 4 - SET THE COLOR OF LINE (OPTIONAL)  ////////////
////////////////////////////////////////////////////////////
ctx.strokeStyle = "teal"; // line color
ctx.lineWidth = 5; // line width
////////////////////////////////////////////////////////////
/////  STEP 5 - MOVE THE "PEN" TO STARTING POINT  //////////
////////////////////////////////////////////////////////////
ctx.moveTo(250, 50);
////////////////////////////////////////////////////////////
/////         STEP 6 - START DRAWING THE DOTS     //////////
////////////////////////////////////////////////////////////
ctx.lineTo(350, 200);
ctx.lineTo(150, 200);
ctx.lineTo(250, 50);
////////////////////////////////////////////////////////////
/////           STEP 7 - DRAW THE PLAN            //////////
////////////////////////////////////////////////////////////
ctx.stroke();
////////////////////////////////////////////////////////////
/////           OPTIONALLY FILL SHAPE             //////////
////////////////////////////////////////////////////////////
ctx.fillStyle = "red";
ctx.fill();

ctx.beginPath();
ctx.moveTo(350, 200);
ctx.lineTo(450, 350);
ctx.lineTo(250, 350);
ctx.lineTo(350, 200);

ctx.stroke();
ctx.fill();
////////////////////////////////////////////////////////////
////            LET'S DO SOMETHING POINTLESS          //////
////////////////////////////////////////////////////////////

ctx.beginPath();
ctx.arc(250, 250, 40, 0, 2 * Math.PI);

ctx.stroke();
ctx.fill();
